public class Escape {

    public static void main(String[] args) {
        System.out.println("Sushmoy \"Nandi\"");
        System.out.println("Sushmoy \\Nandi\\");
        System.out.println("Sushmoy \nNandi");
        System.out.println("Sushmoy \tNandi");
        System.out.println("Sushmoy\bNandi");
    }
}